let turn = 0;
let correctQuestions = 0;
let failedQuestions = 0;
let fase = 0;
let round = questions[fase];
let rankingPlayers = [
    { name: "Aitor", palabrasAcertadas: 9 },
    { name: " Adrian", palabrasAcertadas: 1 },
    { name: "Ignasi", palabrasAcertadas: 15 },
    { name: "Eva", palabrasAcertadas: 3 },
];

const getUserName = () => {
    playerName = prompt("Cual es tu nombre de usuario?");
    alert(`BIENVENIDO ${playerName} AL PASAPALABRA ISDI CODERS!! `);
    if (!playerName) {
        return getUserName();
    }
    alert("CONTESTA LAS PREGUNTAS CON LA PALABRA CORRECTA");
    alert(
        "PUEDES ESCRIBIR 'PASAPALABRA' SI QUIERES SALTAR LA PREGUNTA Y RESPONDERLA EN LA SIGUIENTE RONDA.\nESCRIBE 'END' SI QUIERES ACABAR EL JUEGO"
    );
    alert("COMENCEMOS A JUGAR!!");
};

function main() {
    getUserName();
    pasapalabra(round);
    check();
}

function pasapalabra(round) {
    for (let i = 0; i < round.length; i++) {
        if (round[i].status === 0) {
            let playerAnswer = prompt(`${round[i].question}`);

            while (!playerAnswer) {
                playerAnswer = prompt(`${round[i].question}`);
            }
            if (playerAnswer.toLowerCase() === round[i].answer) {
                correctQuestions++;
                turn++;
                round[i].status = 1;
                alert("PALABRA CORRECTA!!");
            } else if (playerAnswer.toLowerCase() === "pasapalabra") {
                turn++;
                alert("PASAMOS A LA SIGUIENTE PREGUNTA");
            } else if (
                playerAnswer.toLocaleLowerCase() !== round[i].answer &&
                playerAnswer !== "end"
            ) {
                turn++;
                failedQuestions++;
                alert(`Has fallado, la respuesta era: ${round[i].answer}`);
            } else if (
                playerAnswer.toLowerCase() === "end" &&
                playerAnswer !== null
            ) {
                alert(`HAS ACERTADO: ${correctQuestions} PALABRAS`);
                alert(`Que tengas un buen dia ${playerName}`);
                break;
            }
        }
    }
}

function check() {
    if (round.every((w) => w.status === 1)) {
        ranking();
        let seguir = confirm("Quieres continuar?");
        if (seguir === true) {
            fase += 1;
            round = questions[fase];
            questions.shift();
            if (questions.length === 1) {
                ranking();
            } else {
                alert(
                    `Pasamos al nivel ${
                        fase + 1
                    } de pasapalabra, ahora un poco mas dificil`
                );
                pasapalabra(round);
            }
        } else if (seguir === false) {
            alert("Que tenga un buen dia, hasta la proxima!");
        }
    } else {
        pasapalabra(round);
    }
}

function ranking() {
    rankingPlayers.push({
        name: playerName,
        palabrasAcertadas: correctQuestions,
    });

    rankingPlayers.sort(function (a, b) {
        return b.palabrasAcertadas - a.palabrasAcertadas;
    });
}

main();
